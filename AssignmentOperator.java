public class AssignmentOperator {
    public static void main(String[] args) {
        // create variables
        int a =100;
        int var;

        // assign value using =
        var=a;
        System.out.println("var using=" +var);

        // assign value using +=
        var+=a;
        System.out.println("var using=" +var);

        // assign value using *=
        var*=a;
        System.out.println("var using=" +var);

        // RelationalOperators 
        // create variables
        int A=9, b=12;

        // value comparison of A and b
        System.out.println(A==b);
    }
}
